    private void asignarCombo(){
        try {
            DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
            ResultSet rs = tareasBaseDatos.consultar("SELECT * FROM Procedimiento order by cod_pro");
            while (rs.next()) {
                modeloCombo.addElement(rs.getObject("cod_pro"));
            }
            rs.close();
            jComboBox1.setModel(modeloCombo);
        } 
        catch (Exception ex) {
            System.out.println(ex);
        }
    }

