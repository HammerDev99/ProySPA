    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try{
            Procedimiento obj=new Procedimiento();
            int x=(int) jComboBox1.getSelectedItem();
            obj.setCod_pro(x);
            if(tareasBaseDatos.consultarProcedimiento(obj)){
                int eliminar=JOptionPane.showConfirmDialog(this,"¿Esta seguro que desea BORRAR?", "Atencion",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                System.out.println(""+ eliminar);
                if(eliminar==JOptionPane.OK_OPTION){
                    if(tareasBaseDatos.eliminarProcedimiento(obj)){
                        JOptionPane.showMessageDialog(this,"Datos Eliminados");
                    }
                    else{
                         JOptionPane.showMessageDialog(this,"Error al eliminar");   
                    }
                    tareasBaseDatos.listar("SELECT * FROM Procedimiento order by cod_pro",modelo);
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"El codigo de procedimiento no existe");
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Los datos ingresados están erróneos");   
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

